export type TetrominoType = 'I' | 'O' | 'T' | 'S' | 'Z' | 'J' | 'L';

export interface Position {
  x: number;
  y: number;
}

export interface Tetromino {
  type: TetrominoType;
  position: Position;
  rotation: number;
  shape: number[][];
}

export interface Cell {
  filled: boolean;
  color: string;
}

export type Board = Cell[][];

export interface GameState {
  board: Board;
  currentPiece: Tetromino | null;
  nextPiece: TetrominoType;
  score: number;
  lines: number;
  linesCleared: number;
  level: number;
  gameOver: boolean;
  isPaused: boolean;
  dropTime: number;
}

export interface GameConfig {
  boardWidth: number;
  boardHeight: number;
  initialDropTime: number;
  dropTimeDecrement: number;
  minDropTime: number;
  scoreMultiplier: {
    single: number;
    double: number;
    triple: number;
    tetris: number;
  };
}

